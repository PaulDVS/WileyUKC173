package com.sujata.overriding;

public class BaseClass {
	
	private void dummy() {
		System.out.println("Hi I am dummy method of BaseClass!");
	}

	void show() {
		System.out.println("Hi I am show method of BaseClass!");
	}
	
	protected void print() {
		System.out.println("Hi I am print method of BaseClass!");
	}
	
	public void display() {
		System.out.println("Hi I am display method of BaseClass!");
	}
}
